package test;

public class ExplicitTypeCastingExample {
	
	public static void main(String[] args) {
		int i=9;
		System.out.println(i);
		long l =9;
		System.out.println(l);
		float f=9;
		System.out.println(f);
		double d = 9;
		System.out.println(d);
		//Explicit Type casting
		short s=9;
		byte b=9;
	}

}
